#include "minishell.h"

int	builtin_echo(char **argv)
{
	int		i;
	bool	newline;

	i = 1;
	newline = true;
	if (argv[1] && !ft_strcmp(argv[1], "-n"))
	{
		newline = false;
		i++;
	}
	while (argv[i])
	{
		ft_putstr_fd(argv[i], STDOUT_FILENO);
		if (argv[i + 1])
			ft_putchar_fd(' ', STDOUT_FILENO);
		i++;
	}
	if (newline)
		ft_putchar_fd('\n', STDOUT_FILENO);
	return (0);
}
